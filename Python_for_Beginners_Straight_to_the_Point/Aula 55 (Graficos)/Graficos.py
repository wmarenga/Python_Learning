""" 
Os graficos sao a melhor maneira de mostrar um volume grande de dados:

Tipos de graficos:

Barras:

Sao utilizados para categoria ou tempo contra dado numerico, Este grafico e bom
para mostrar comportamentos de anomalia. Este grafico e o mais usado pois e de facil visualizacao. 
No eixo horizontal nos descrevemos uma categoria ou tempo e no eixo vertical sao apresentados os
dados numericos.

Linhas:

Sao utilizados para mostrar dados de tempo contra dado numerico ou ate mesmo analise de 2 dados 
numericos. Este grafico e bom para mostrar tendencia ou ruido. Evitar trabalhar com categorias no
grafico de linhas. 

Dispersao:

Normalmente usado para analise de 2 dados numericos (ou tempo em um eixo). Este grafico e bom para 
mostrar correlacao entre dados. Existe uma tendencia, porem alguns dados estao fora desta tendencia, 
sendo assim uma linha nao seria adequado neste caso. 

Pizza:

Este grafico e usado para comparar porcentagens de um dado categorico. Somente usar este grafico com
porcentagem. Evitar usar este grafico quando a porcentagem das categorias foram muito proximas. 

Boxplot:

Este grafico e utilizado categoria contra dado numerico. Ele mostra a variacao dos dados e outliers. 
O grafico Boxplot e muito usado para dados estatisticos (media, mediana, quartis, limites - inferiores 
e superiores, etc) e sendo assim e muito bom para buscar Outlierse avaliar se uma variavel impacta 
no comportamento de outra. 

Histograma e Curva de Distribuicao normal:

Este grafico tambem e muito usado em estatistica e normalmente utilizado para analise de 2 dados 
numericos. Um dos dados devera ter uma media ou valor esperado. Assim como o grafico de barras, no
eixo horizontal nos teremos uma categoria, tempo, dado numerico e no eixo vertical uma frequencia ou 
quanto este dados estao ocorrendo.

Bolhas:

Este grafico e similar ao grafico de dispersao, porem uma terceira dimensao e adicionada. Neste grafico
podemos mostrar 3 dimensoes de dados em um eixo 2D. 

Pareto:

E usado para categoria contra dado numerico. Otimo para identificar se existe categorias que sao muito
significativas. Mostra as categorias mais representativas no conjunto de dados. Sao geradas colunas em ordem
decrescente de frequencia e uma linha mostrando os percentagens acumuladas destas categorias.

Dendograma:

Mostra a frequencia de um dado dentro de diversas categorias. 

Diagrama de Sankey:

Mostra o relacionamento entre 2 variaveis. E muito bom para verificar se existe uma relacao entre o 
evento A e B para avaliar se dados sao exclusivos ou nao e se e possivel separa-los ou nao. 

Mapa de Calor:

Mostra o comportamento de uma variavel comparada a outras 2. Neste grafico e possivel mostrar a 
intensidade de uma terceira variavel plotada sobre um grafico 2D. Este grafico e muito bom para
trazer o impacto visual sobre o nosso problema. Podemos ter dados continuos nos 2 eixos, porem nos
tambem podemos usar categorias neste grafico.
"""