# Aditivas
valor_Adit = 4
valor_Adit += 5
print(valor_Adit)

# Subtrativa
valor_Sub = 3
valor_Sub -= 1
print(valor_Sub)

# Multiplicativa
valor_Mult = 5
valor_Mult *= 2
print(valor_Mult)

# Divisiva
valor_Div = 12
valor_Div /= 2
print(valor_Div)

# Modulo de Divisao (Resto da divisao)
valor_Mod = 12
valor_Mod %= 4
print(valor_Mod)

# Divisao Inteira
valor_Int = 512
valor_Int //= 256
print(valor_Int)

# Exponenciacao
valor_Exp = 4
valor_Exp **= 8
print(valor_Exp)
