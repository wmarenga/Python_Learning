# Estrutura Condicional Simples

# if

nome = input('Digite o seu nome: ')

if nome == 'Fernando':
    print(f'Bem vindo de volta {nome}!!!')
if nome != 'Fernando':
    print(f'Voce e novo(a) aqui, ola {nome}!!!')

''' Se a condicao for atingida, o bloco dela e executado, caso contrario e simplesmente ignorado'''
